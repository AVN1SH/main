export type LangCode = keyof typeof translations;

let currentLang: LangCode = 'en';
if (typeof window !== 'undefined') {
    currentLang = (localStorage.getItem('whirly_lang') as LangCode) || 'en';
}

export const setLang = (lang: LangCode) => {
    currentLang = lang;
    if (typeof window !== 'undefined') {
        localStorage.setItem('whirly_lang', lang);
    }
};

export const getLang = () => currentLang;

export const t = (key: keyof typeof translations['en']) => translations[currentLang][key] || translations['en'][key];

